function fillTemplate(template, vocab) {
  return template.replace(/\{(\w+)\}/g, (match, key) => {
    if (vocab[key] && vocab[key].length > 0) {
      return pickRandom(vocab[key])
    }
    return match
  })
}

function generatePoem(type, theme, userInput) {
  const themes = detectThemes(userInput)
  const primaryTheme = themes.includes(theme) ? theme : themes[0]
  const vocab = getVocab(primaryTheme)

  let typeKey = type
  if (type.includes('五言') && type.includes('绝句')) typeKey = '五言绝句'
  else if (type.includes('七言') && type.includes('绝句')) typeKey = '七言绝句'
  else if (type.includes('五言') && type.includes('律诗')) typeKey = '五言律诗'
  else if (type.includes('七言') && type.includes('律诗')) typeKey = '七言律诗'
  else if (type.includes('词')) typeKey = '词'
  else if (type.includes('自由')) typeKey = '自由体诗'

  const typeTemplates = templatePool[typeKey] || templatePool['五言绝句']
  const themeTemplates = typeTemplates[primaryTheme] || typeTemplates['default']
  const template = pickRandom(themeTemplates)

  if (Array.isArray(template)) {
    return template.map(line => fillTemplate(line, vocab)).join('，').replace(/，([^，]+)$/, '。$1').replace(/([^。？！；，]+)$/, (m, p1) => {
      if (p1 && !p1.match(/[。？！；]$/)) return p1 + '。'
      return m
    })
  }

  return fillTemplate(template, vocab)
}

